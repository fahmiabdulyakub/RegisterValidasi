export const regex = {
  uppercase: /^(?=.*[A-Z])/,
  lowercase: /^(?=.*[a-z])/,
  number: /^(?=.*[0-9])/,
  character: /^(?=.{9,})/,
  email: /^(([^<>()[\]\\.,;:\s@\"]+(\.[^<>()[\]\\.,;:\s@\"]+)*)|(\".+\"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
};
